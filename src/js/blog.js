// Render blog list
function renderBlogList() {
  const container = document.getElementById("blogList");

  blogPosts.forEach((post) => {
    const article = document.createElement("article");
    article.className =
      "bg-white rounded-2xl shadow-lg overflow-hidden mb-8 hover:shadow-xl transition duration-300";

    article.innerHTML = `
                    <div class="md:flex">
                        <div class="md:w-2/5">
                            <img src="${post.image}" alt="${post.title}" class="w-full h-64 md:h-full object-cover">
                        </div>
                        <div class="md:w-3/5 p-8">
                            <div class="mb-3">
                                <span class="inline-block bg-orange-100 text-orange-600 text-xs font-semibold px-3 py-1 rounded-full uppercase tracking-wide">${post.category}</span>
                            </div>
                            <h2 class="text-2xl md:text-3xl font-bold text-gray-900 mb-4 hover:text-orange-600 transition duration-300 cursor-pointer" data-post-id="${post.id}">
                                ${post.title}
                            </h2>
                            <div class="flex items-center text-sm text-gray-500 mb-4">
                                <span>POSTED ON ${post.date} BY ${post.author}</span>
                            </div>
                            <p class="text-gray-700 leading-relaxed mb-6">
                                ${post.excerpt}
                            </p>
                            <button class="read-more-btn inline-block border-2 border-orange-500 text-orange-600 hover:bg-orange-500 hover:text-white font-semibold px-6 py-2 rounded-lg transition duration-300" data-post-id="${post.id}">
                                CONTINUE READING â†’
                            </button>
                            <div class="mt-6 pt-6 border-t border-gray-200">
                                <span class="text-sm text-gray-500">Posted in <span class="text-orange-600">${post.category}</span></span>
                            </div>
                        </div>
                    </div>
                `;

    container.appendChild(article);
  });

  // Add event listeners
  document.querySelectorAll(".read-more-btn").forEach((btn) => {
    btn.addEventListener("click", function () {
      const postId = parseInt(this.getAttribute("data-post-id"));
      showDetail(postId);
    });
  });

  document.querySelectorAll("h2[data-post-id]").forEach((title) => {
    title.addEventListener("click", function () {
      const postId = parseInt(this.getAttribute("data-post-id"));
      showDetail(postId);
    });
  });
}

// Show detail
function showDetail(postId) {
  const post = blogPosts.find((p) => p.id === postId);
  if (!post) return;

  // Hide list, show detail
  document.getElementById("blogListSection").classList.add("hidden");
  document.getElementById("blogDetailSection").classList.remove("hidden");

  // Update content
  document.getElementById("detailCategory").textContent = post.category;
  document.getElementById("detailTitle").textContent = post.title;
  document.getElementById(
    "detailMeta"
  ).innerHTML = `<span>POSTED ON ${post.date} BY ${post.author}</span>`;
  document.getElementById("detailImage").src = post.image;
  document.getElementById("detailImage").alt = post.title;
  document.getElementById("detailContent").innerHTML = post.content;

  // Render latest posts
  renderLatestPosts(postId);

  // Scroll to top
  window.scrollTo({ top: 0, behavior: "smooth" });
}

// Render latest posts
function renderLatestPosts(currentPostId) {
  const container = document.getElementById("latestPosts");
  const posts = blogPosts.filter((p) => p.id !== currentPostId).slice(0, 5);

  container.innerHTML = posts
    .map(
      (post) => `
                <div class="border-b border-gray-200 pb-4 last:border-0 last:pb-0 cursor-pointer" data-post-id="${
                  post.id
                }">
                    <div class="flex items-center text-xs text-gray-500 mb-2">
                        <span class="bg-orange-100 text-orange-600 px-2 py-1 rounded mr-2">${
                          post.date.split("/")[0]
                        }</span>
                        <span class="text-gray-400">${
                          post.date.split("/")[1]
                        }</span>
                    </div>
                    <h4 class="text-sm font-semibold text-gray-900 hover:text-orange-600 transition duration-300">
                        ${post.title}
                    </h4>
                    <p class="text-xs text-gray-500 mt-1">Comments Off</p>
                </div>
            `
    )
    .join("");

  // Add click listeners
  container.querySelectorAll("[data-post-id]").forEach((el) => {
    el.addEventListener("click", function () {
      const postId = parseInt(this.getAttribute("data-post-id"));
      showDetail(postId);
    });
  });
}

// Back to list
document.getElementById("backButton").addEventListener("click", function () {
  document.getElementById("blogListSection").classList.remove("hidden");
  document.getElementById("blogDetailSection").classList.add("hidden");
  window.scrollTo({ top: 0, behavior: "smooth" });
});

// Initialize
renderBlogList();
